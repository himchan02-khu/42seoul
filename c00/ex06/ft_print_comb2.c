#include <unistd.h>

void	ft_print_comb2(void)
{
	char	c;
	int		a;
	int		b;

	a = 0;
	while (a <= 97)
	{
		b = a + 1;
		while (b <= 99)
		{
			c = a / 10 + '0';
			write (1, &c, 1);
			c = a % 10 + '0';
			write (1, &c, 1);
			write (1, " ", 1);
			c = b / 10 + '0';
			write (1, &c, 1);
			c = b % 10 + '0';
			write (1, &c, 1);
			write (1, ", ", 2);
			b++;
		}
		a++;
	}
	write (1, "98 99", 5);
}
