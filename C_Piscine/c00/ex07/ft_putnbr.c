#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		n;
	char	c;

	n = 1000000000;
	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -1 * nb;
	}
	while (nb > 0 && n > 1)
	{
		c = (nb / n) % 10 + '0';
		if (nb / n != 0)
			write (1, &c, 1);
		n = n / 10;
	}
	c = nb % 10 + '0';
	write (1, &c, 1);
}
